package Day2_09052020;

public class Dynamic_Array
{
    public static void main(String[] args)
    {
        String[] zipCode = new String[5];
        zipCode[0] = "11230";
        zipCode[1] = "11231";
        zipCode[2] = "11232";
        zipCode[3] = "11233";
        zipCode[4] = "11234";
        System.out.println("\nMy current zip code is " + zipCode[0]);



        int[] checkAmount = new int[6];
        checkAmount[0] = 100;
        checkAmount[1] = 200;
        checkAmount[2] = 300;
        checkAmount[3] = 400;
        checkAmount[4] = 500;
        checkAmount[5] = 600;
        System.out.println("\nMy last checkAmount was " + checkAmount[4]);

    }
}
